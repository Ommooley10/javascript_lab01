//console.log(2^4)=6
/*In JavaScript, the ^ operator is not used for exponentiation. Instead, it is the bitwise XOR operator.
Here's what happens when you do console.log(2^4):
2 in binary is 10.
4 in binary is 100.
When you perform a bitwise XOR operation (^), it compares each corresponding bit of the two numbers. If the bits are different, it returns 1; if they are the same, it returns 0.
So, 10 ^ 100 results in 110, which is 6 in decimal.*/